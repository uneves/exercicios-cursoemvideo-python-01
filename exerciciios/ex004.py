print('"{}" é um número? '.format(n), n.isnumeric())
print('"{}" é alfabetico? '.format(n), n.isalpha())
print('"{}" é alfanumerico? '.format(n), n.isalnum())
print('"{}" possui somente possui letras maiusculas? '.format(n), n.isupper())
print('"{}" possui somente possui letras minusculas? '.format(n), n.islower())
print('"{}" está capitalizado - possui letras minusculas e minusculas? '.format(n), n.istitle())
print('"{}" pode ser impresso? '.format(n), n.isprintable())
print('"{}" é um numero decimal? '.format(n), n.isdecimal())
print('"{}" pode ser identificado? '.format(n), n.isidentifier())
print('"{}" é composto somente de espacos? '.format(n), n.isspace())
